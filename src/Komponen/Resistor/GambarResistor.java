    private void OkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OkMouseClicked
        // TODO add your handling code here:
        String hasil2 = manualarea.getText();
        long nilaihambatanmanual =(long) Double.parseDouble(hasil2);
        if(nilaihambatanmanual >= 1000){
            double resis =(double) nilaihambatanmanual/1000;
            String hasil = Double.toString(resis);
            outputnilai.setText("Nilai = "+hasil+" KOhm");
        }
        else{
            String hasil = Long.toString(nilaihambatanmanual);
            outputnilai.setText("Nilai = "+hasil+" Ohm");
        }
        
        InitCincinOtomatis.setNilai(nilaihambatanmanual);
        InitCincinOtomatis.getCincinOtomatis();
        //method untuk main page
        long nilaihambatan2 = NilaiResistor.getHambatanMinimum();
        Hitung.setHambatan(nilaihambatan2);
        Plot.setHambatan(nilaihambatan2);
        PlotDasar.setHambatan(nilaihambatan2);
        RangkaianRC.labelresistor.setText(Long.toString(nilaihambatan2)+ " Ohm");
        //end
    }//GEN-LAST:event_OkMouseClicked

