    private void hitungMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hitungMouseClicked
        // TODO add your handling code here:
        long nilaihambatan = NilaiResistor.getHambatanMinimum();
        if(nilaihambatan >= 1000){
            double resis =(double) nilaihambatan/1000;
            String hasil = Double.toString(resis);
            outputnilai.setText("Nilai = "+hasil+" KOhm");
        }
        else{
            String hasil = Long.toString(nilaihambatan);
            outputnilai.setText("Nilai = "+hasil+" Ohm");
        }
        //Hitung.setHambatan(nilaihambatan);
        Hitung.setHambatan(nilaihambatan);
        Plot.setHambatan(nilaihambatan);
        PlotDasar.setHambatan(nilaihambatan);
        RangkaianRC.labelresistor.setText(Long.toString(nilaihambatan)+ " Ohm");
        //setVisible(false);
    }//GEN-LAST:event_hitungMouseClicked

